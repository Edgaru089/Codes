template<typename IntType>
void read(IntType& val) {
	val = 0;
	int c;
	bool inv = false;
	while (!isdigit(c = getchar()))
		if (c == '-')
			inv = true;
	do {
		val = (val << 1) + (val << 3) + c - '0';
	} while (isdigit(c = getchar()));
	if (inv)
		val = -val;
}

template<>
void read<string>(string& str){
	str.clear();
	int c;
	while(iscntrl(c=getchar())||c==' '||c=='\t');
	do{
		str.push_back(c);
	}while(!(iscntrl(c=getchar())||c==' '||c=='\t'));
}

const int MaxN=7+2,MaxM=21+10;
int n,m;
int deg[MaxN];
int mask[MaxN];

int popcnt(int x){
	int ans=0;
	while(x){
		if(x&1)
			ans++;
		x>>=1;
	}
	return ans;
}


int main(int argc, char* argv[]) {

	read(n);read(m);
	for(int i=1;i<=m;i++){
		int u,v;
		read(u);read(v);
		deg[u]++;
		deg[v]++;
		mask[u]|=(1<<v);
		mask[v]|=(1<<u);
	}

	if(n<=6)
		printf("%d\n",m);
	else{
		int ans=0;
		for(int i=1;i<n;i++){
			for(int j=i+1;j<=n;j++){
				ans=max(ans,m-popcnt(mask[i]&mask[j]));
			}
		}
		printf("%d\n",ans);
	}

	return 0;
}


