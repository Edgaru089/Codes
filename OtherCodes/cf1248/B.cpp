template<typename IntType>
void read(IntType& val) {
	val = 0;
	int c;
	bool inv = false;
	while (!isdigit(c = getchar()))
		if (c == '-')
			inv = true;
	do {
		val = (val << 1) + (val << 3) + c - '0';
	} while (isdigit(c = getchar()));
	if (inv)
		val = -val;
}

template<>
void read<string>(string& str){
	str.clear();
	int c;
	while(iscntrl(c=getchar())||c==' '||c=='\t');
	do{
		str.push_back(c);
	}while(!(iscntrl(c=getchar())||c==' '||c=='\t'));
}

typedef long long ll;
const int MaxN=1e5+10;

ll n;

pair<ll,int> a[MaxN];




int main(int argc, char* argv[]) {

	read(n);
	for(int i=1;i<=n;i++){
		read(a[i].first);
		a[i].second=i;
	}

	sort(a+1,a+n+1);

	ll mid=n/2;
	ll x=0,y=0;

	if(n&1){
		for(int i=1;i<=mid;i++)
			x+=a[i].first;
		for(int i=mid+2;i<=n;i++)
			y+=a[i].first;
		if(x>y)
			x+=a[mid+1].first;
		else
			y+=a[mid+1].first;
		cout<<x*x+y*y<<endl;
	}else{
		for(int i=1;i<=mid;i++)
			x+=a[i].first;
		for(int i=mid+1;i<=n;i++)
			y+=a[i].first;
		cout<<x*x+y*y<<endl;
	}

	return 0;
}


